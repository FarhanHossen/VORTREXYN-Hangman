/**
 * lib/firebase.ts
 *
 * Initialises the Firebase SDK for the VORTREXYN Hangman app.
 *
 * Why a single file for this?
 * Firebase must only be initialised once per JS runtime.  Exporting `auth`
 * and `db` from here gives every module a shared, singleton reference to the
 * same Firebase services without risking double-initialisation errors.
 *
 * Auth persistence strategy:
 *  - Native (iOS / Android): `getReactNativePersistence(AsyncStorage)` — saves
 *    the logged-in session to the device's AsyncStorage so users stay signed in
 *    after closing the app.
 *  - Web: `browserLocalPersistence` — saves the session in localStorage.
 *    A try/catch wraps the call because `initializeAuth` throws if it has
 *    already been called for this app instance (e.g. hot-reload in dev).
 */

