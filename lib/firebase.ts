/**
 * Initialise the Firebase app exactly once.
 * `getApps()` returns an array of already-initialised apps, so if the array
 * is non-empty we reuse the existing instance (important during hot-reload).
 */
const app = getApps().length === 0 ? initializeApp(firebaseConfig) : getApps()[0];

/**
 * `auth` — Firebase Authentication instance.
 *
 * Platform-aware initialisation:
 *  - Web  → `browserLocalPersistence` keeps the user logged in via localStorage.
 *  - Native → `getReactNativePersistence(AsyncStorage)` persists the session
 *             across app restarts using React Native's AsyncStorage.
 */
export const auth =
  Platform.OS === "web"
    ? (() => {
        try {
          // Try to create a new auth instance with browser-local persistence.
          return initializeAuth(app, { persistence: browserLocalPersistence });
        } catch {
          // If initializeAuth already ran (hot-reload), fall back to getAuth.
          return getAuth(app);
        }
      })()
    : initializeAuth(app, {
        // On native devices, persist the auth token in AsyncStorage
        // so users stay signed in after force-quitting the app.
        persistence: getReactNativePersistence(AsyncStorage),
      });

/**
 * `db` — Firestore database instance.
 * Used across the app to read and write user profiles and game statistics.
 */
export const db = getFirestore(app);
